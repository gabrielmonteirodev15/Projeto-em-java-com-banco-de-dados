    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // Salvar
        //1 Passo - receber os dados da tela
        Cliente obj = new Cliente();
        obj.setNome(txtnome.getText());
        obj.setRg(txtrg.getText());
        obj.setCpf(txtcpf.getText());
        obj.setEmail(txtemail.getText());
        obj.setTelefone(txttel.getText());
        obj.setCelular(txtcel.getText());
        obj.setCep(txtcep.getText());
        obj.setEndereco(txtend.getText());
        obj.setNumero(Integer.parseInt(txtnum.getText()));
        obj.setComplemento(txtcom.getText());
        obj.setBairro(txtbairro.getText());
        obj.setCidade(txtcidade.getText());
        obj.setEstado(cbestado.getSelectedItem().toString());
        
        //2 Passo - Criar o objeto Dao
        
        ClienteDAO dao = new ClienteDAO();
        dao.cadastrarCliente(obj);
    }//GEN-LAST:event_btnsalvarActionPerformed

