    public void CarregarTabela(){
        try {
            
            //1 Passo - Instaciar um objeto da classe ClienteDAO
            
            ClienteDAO dao = new ClienteDAO();
            
            List<Cliente> lista = dao.ListarClientes();
            
            // 2 Passo - Criar DefaulTableModel
            DefaultTableModel conteudo = (DefaultTableModel)tabelaClientes.getModel();
            
            conteudo.setNumRows(0);
            
            //3 Passo - Montar o conteudo para exibir na tabela
            for(Cliente cliente: lista){
                conteudo.addRow(new Object[]{
                    cliente.getId(),
                    cliente.getNome(),
                    cliente.getRg(),
                    cliente.getCpf(),
                    cliente.getEmail(),
                    cliente.getTelefone(),
                    cliente.getCelular(),
                    cliente.getCep(),
                    cliente.getEndereco(),
                    cliente.getComplemento(),
                    cliente.getNumero(),
                    cliente.getBairro(),
                    cliente.getEstado()  
                });
                
            }
        } catch (Exception e) {
        }
    }
